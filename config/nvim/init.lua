vim.keymap.set("i", "<up>", "<C-o>gk")
vim.keymap.set("v", "<up>", "gk")
vim.keymap.set("n", "<down>", "gj")
vim.keymap.set("i", "<down>", "<C-o>gj")
vim.keymap.set("v", "<down>", "gj")

